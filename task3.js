class calculate {
    // Add method
    static add(a, b) {
      return a + b;
    }
  
    // Substract method
    static subtract(a, b) {
      return a - b;
    }
  
    // Divide method
    static divide(a, b) {
      return a / b;
    }
  
    // Multiply method
    static multiply(a, b) {
      return a * b;
    }
  }
  
  module.exports = calculate;
  
  